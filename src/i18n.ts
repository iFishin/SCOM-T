const dicts: Record<Lang, Dict> = { zh: ZH, en: EN };

export function t(
  key: string,
  lang: Lang,
  ...args: (string | number)[]
): string {
  const text = dicts[lang][key] ?? key;
  if (args.length) {
    let i = 0;
    return text.replace(/%[sd]/g, () => String(args[i++]));
  }
  return text;
}

export function useLang(settingsLang?: Lang): Lang {
  if (settingsLang === "en" || settingsLang === "zh") return settingsLang;
  // Try browser language; fallback to zh
  try {
    const nav = navigator.language;
    if (nav.startsWith("en")) return "en";
  } catch {
    /* ignore */
  }
  return "zh";
}
